public class Exam17 {

	public static void main(String[] args) {
		
		System.out.println("How are you?");
		System.out.println("How are you?");
		System.out.println("How are you?");
		System.out.println("\n");
		
		for(int i=1;i<=10;i++) {   //  초기값;조건값;증감값 
			System.out.println("How are you?");
		}
		System.out.println();
		
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
	}
}






















